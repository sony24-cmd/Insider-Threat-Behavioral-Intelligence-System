from sqlalchemy.orm import Session

from models.risk_score import RiskScore
from models.alert import Alert
from models.notification import Notification

from services.ai_service import predict_risk


def analyze_employee(
    db: Session,
    employee_id: int,
    login_count: int,
    logout_count: int,
    usb_connect: int,
    usb_disconnect: int,
    http_visits: int,
    total_events: int,
):
    """
    Analyze employee behaviour,
    save Risk Score,
    create Alert,
    create Notification.
    """

    result = predict_risk(
        login_count,
        logout_count,
        usb_connect,
        usb_disconnect,
        http_visits,
        total_events,
    )

    # Convert confidence (0-100) to score
    score = result["confidence"]

    risk = RiskScore(
        employee_id=employee_id,
        risk_score=score,
        risk_level=result["risk_level"],
        remarks="Generated by AI Model",
    )

    db.add(risk)
    db.commit()
    db.refresh(risk)

    # High Risk → Generate Alert
    if result["prediction"] == 1:

        alert = Alert(
            employee_id=employee_id,
            risk_score_id=risk.id,
            alert_type="AI Insider Threat",
            severity="High",
            description="AI detected suspicious employee behaviour.",
            status="Open",
        )

        db.add(alert)

        notification = Notification(
            title="High Risk Employee",
            message=f"Employee {employee_id} has been classified as HIGH RISK.",
            severity="High",
        )

        db.add(notification)

        db.commit()

    return {
        "risk_id": risk.id,
        "prediction": result["prediction"],
        "confidence": result["confidence"],
        "risk_level": result["risk_level"],
    }